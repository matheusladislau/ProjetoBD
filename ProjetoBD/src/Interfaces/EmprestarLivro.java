    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
        int idEmprestimo=bd.MaxIdEmprestimo()+1;
        int idPessoa=Integer.parseInt(cmp_recebeIdPessoa.getText());
        Calendar c=Calendar.getInstance();
        int ano=c.get(Calendar.YEAR);
        int mes=1+c.get(Calendar.MONTH);
        int dia=c.get(Calendar.DAY_OF_MONTH);
        String dataEmprestimo=ano+"-"+mes+"-"+dia;
        String dataDevolucao="";
        if(mes<12){// o prazo para devolu√ß√£o ser√° de um m√™s
            dataDevolucao=ano+"-"+(mes+1)+"-"+dia;
        }else{
            dataDevolucao=(ano+1)+"-"+mes+"-"+dia;
        }            
        if(bd.insert(new Emprestimo(idEmprestimo,idPessoa,dataEmprestimo,dataDevolucao,false))){
            JOptionPane.showMessageDialog(null,"Empr√©stimo de c√≥digo "+idEmprestimo+" realizado com sucesso.");
            limpar();
        }else{
            JOptionPane.showMessageDialog(null,"Empr√©stimo n√£o realizado.");
        }
        if(idLivro1!=-1)
            bd.insert(new LivroEmprestimo(idEmprestimo,idLivro1));
        if(idLivro2!=-1)
            bd.insert(new LivroEmprestimo(idEmprestimo,idLivro2));
        if(idLivro3!=-1)
            bd.insert(new LivroEmprestimo(idEmprestimo,idLivro3));
    }//GEN-LAST:event_btn_enviarActionPerformed

